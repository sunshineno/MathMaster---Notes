const CACHE_NAME = "mathmaster-v0.3.3";
const APP_SHELL = [
  "/MathMaster---Notes/",
  "/MathMaster---Notes/index.html",
  "/MathMaster---Notes/manifest.webmanifest",
  "/MathMaster---Notes/icon-192.png",
  "/MathMaster---Notes/icon-512.png"
];

self.addEventListener("install", event => {
  event.waitUntil(
    caches.open(CACHE_NAME).then(cache => cache.addAll(APP_SHELL))
  );
  self.skipWaiting();
});

self.addEventListener("activate", event => {
  event.waitUntil(
    caches.keys().then(keys =>
      Promise.all(keys.filter(key => key !== CACHE_NAME).map(key => caches.delete(key)))
    )
  );
  self.clients.claim();
});

self.addEventListener("fetch", event => {
  if (event.request.method !== "GET") return;

  event.respondWith(
    caches.match(event.request).then(cached => {
      if (cached) return cached;

      return fetch(event.request)
        .then(response => {
          const copy = response.clone();
          caches.open(CACHE_NAME).then(cache => cache.put(event.request, copy));
          return response;
        })
        .catch(() => caches.match("/MathMaster---Notes/index.html"));
    })
  );
});
